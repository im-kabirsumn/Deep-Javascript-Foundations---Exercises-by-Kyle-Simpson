function isValidName(name) {
  if (typeof name === "string") {
    return name.length >= 3 && Number(name) !== 0;
  } else {
    return false;
  }
}


function hoursAttended(attended, length) {
  let attendedNumber = isNum(attended);
  let lengthNumber = isNum(length);

  function isNum(x) {
    if (typeof x === "string" && Number(x) !== 0) {
      return Number(x);
    } else if (typeof x === "number") {
      return x;
    } else {
      return false;
    }
  }

  return Number.isInteger(attendedNumber) && Number.isInteger(lengthNumber) && attendedNumber <= lengthNumber;

}

